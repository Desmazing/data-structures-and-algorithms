# arithmetic ops on variables

a = float(input('Enter 1st number: '))
b = float(input('Enter 2nd number: '))
c = float(input('Enter 3rd number: '))

print('Addition of the numbers = '+str(a+b+c))
print('Subtraction of the numbers = '+str(a-b-c))
print('Multiplication of the numbers = '+str(a*b*c))
print('Division of the numbers = '+str(a/b/c))
print('Modulus of the numbers = '+str(a%b%c))
